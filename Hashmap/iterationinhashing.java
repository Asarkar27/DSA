public class iterationinhashing {
  public static void main(String[] args) {

    int arr[]={12,13,15};
    for(int i =0; i <3;i++){
      System.out.print(arr[i]+" ");
    }
    System.out.println();

    for (int val : arr){
      System.out.print(val + " ");
    }
    System.out.println();
  }
}
